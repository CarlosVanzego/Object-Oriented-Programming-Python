# Strings
# A String can be a collection of alphabets, words, or other characters. They are typically used to store values like names, places or any other combination of alphanumeric or special characters.

# Strings in Python are "immutable" which means they cannot be changed after they are created. You cannot insert or remove a charachter from a string after it is created.

# Strings in Python are surrounded by either single quotation marks or double quotation marks.
# 
# Syntax: 
str = "Hello" 

print(str)

# String length:
str = "Hello there."

print(len(str))

# String concatenation:
word1 = "Hello"
word2 = "World"

sentence = word1 + word2

print(sentence)