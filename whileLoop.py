# While Loop
# 
# While loop:
# With the while loop, we can execute a set of statements as long as a condition is true.
# Ex.
# 
# I initialized the variable 'z' with the value of 4.
z = 4
# I created a while loop that runs as long a 'z' is less than 8.
while z < 8:
  # Inside the loop, I print the current value of 'z'.
  print(z)
  # I then increment 'z' by 1 each iteration, stopping before it reaches 8.
  z += 1