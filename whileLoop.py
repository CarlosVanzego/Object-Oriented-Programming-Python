# While Loop
# 
# While loop:
# With the while loop, we can execute a set of statements as long as a condition is true.
# Ex.
# 
# I initialized the variable 'c' with the value of 4.
c = 5
# I created a while loop that runs as long a 'z' is less than 8.
while c < 11:
  # Inside the loop, I print the current value of 'c'.
  print(c)
  # I then increment 'c' by 1 each iteration, stopping before it reaches 8.
  c += 1