# Graph
# 
# A graph data structure is a collection of nodes that have data and are connected to other nodes.
# It consists of a collection of vertices V and a collection of edges E, represented as ordered pairs of vertices (V,E)

# Adjacency:
# A vertex is considered adjacent to another vertex if they are connected by an edge.

# Path:
# A sequence of edges that allows you to go from vertex A to vertex B is called a path.

# Undirected Graph:
# A graph in which the adjacency relation is symmetric. So if there exists an edge from node u to node v (u -> v), then it is also the case that there exists an edge from node v node u (V -> u)

# Directed Graph:
# A graph in which the adjacency relation is not symmetric. The edges in such a graph are represented by arrows to show the direction of the edge.